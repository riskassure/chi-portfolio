import re
import html as html_lib
import hashlib
from urllib.parse import quote


PSPICTURE_RE = re.compile(
    r"\\begin\{pspicture\}[\s\S]*?\\end\{pspicture\}",
    re.MULTILINE
)


def hash_pstricks_block(block: str) -> str:
    return hashlib.sha256(block.encode("utf-8")).hexdigest()[:16]


def extract_pstricks_blocks(tex: str) -> list[str]:
    return PSPICTURE_RE.findall(tex or "")


def extract_pstricks_hashes(tex: str) -> list[str]:
    return [
        hash_pstricks_block(block)
        for block in extract_pstricks_blocks(tex)
    ]


def get_svg_filename(svg_path: str) -> str | None:
    if not svg_path:
        return None

    clean_path = str(svg_path).replace("\\", "/").rstrip("/")
    return clean_path.split("/")[-1]


def make_diagram_img_tag(svg_filename: str) -> str:
    if not svg_filename:
        return '<div class="img-placeholder"><em>[Diagram path missing.]</em></div>'

    return (
        '<div class="math-diagram-wrap">'
        f'<img src="http://127.0.0.1:5000/api/math/diagrams/{svg_filename}" '
        'class="math-diagram" '
        'alt="Mathematical diagram">'
        '</div>'
    )


def render_pmlinkname(match: re.Match) -> str:
    link_text = match.group(1).strip()
    target_slug = match.group(2).strip()

    return (
        f'<a class="math-explicit-link" '
        f'href="concept.html?slug={quote(target_slug)}">'
        f'{html_lib.escape(link_text)}</a>'
    )


def render_latex_list_block(match: re.Match) -> str:
    """
    Convert one LaTeX itemize/enumerate block into an HTML list.

    This function assumes nested itemize/enumerate blocks have already been
    converted to HTML, so splitting on \\item is safe.
    """
    env_name = match.group(1).lower()
    body = match.group(2).strip()

    html_tag = "ul" if env_name == "itemize" else "ol"

    if not body:
        return match.group(0)

    # Normalize optional labels:
    # \item[foo] text  ->  \item text
    body = re.sub(
        r"\\item\s*\[[^\]]*\]",
        r"\\item",
        body,
        flags=re.IGNORECASE,
    )

    raw_items = re.split(
        r"\\item\b",
        body,
        flags=re.IGNORECASE,
    )

    items = []

    for raw_item in raw_items:
        cleaned = raw_item.strip()

        if not cleaned:
            continue

        items.append(f"<li>{cleaned}</li>")

    if not items:
        return match.group(0)

    return (
        f"<{html_tag}>\n"
        + "\n".join(items)
        + f"\n</{html_tag}>"
    )


def render_latex_lists_to_html(html: str) -> str:
    """
    Convert LaTeX itemize/enumerate environments into HTML lists.

    Important:
        This converts innermost list blocks first so nested lists do not confuse
        the regex matcher.
    """
    list_block_pattern = re.compile(
        r"\\begin\{(itemize|enumerate)\}"
        r"((?:(?!\\begin\{(?:itemize|enumerate)\}).)*?)"
        r"\\end\{\1\}",
        flags=re.IGNORECASE | re.DOTALL,
    )

    previous = None

    while previous != html:
        previous = html
        html = list_block_pattern.sub(render_latex_list_block, html)

    return html


def remove_empty_html_list_items(html: str) -> str:
    """
    Remove empty HTML list items generated by odd LaTeX list structures.

    This is a safety net, not the main list parser.
    """
    return re.sub(
        r"\s*<li>\s*</li>\s*",
        "\n",
        html,
        flags=re.IGNORECASE,
    )


def render_latex_generic_list_block(match: re.Match) -> str:
    """
    Convert a generic LaTeX list environment into an HTML ordered list.

    Example:
        \\begin{list}{...}{}
        \\item first
        \\item second
        \\end{list}

    The list formatting arguments are ignored for now.
    """
    original_block = match.group(0)
    body = match.group(1).strip()

    if not body:
        return original_block

    raw_items = re.split(
        r"\\item\b",
        body,
        flags=re.IGNORECASE,
    )

    items = []

    for raw_item in raw_items:
        cleaned = raw_item.strip()

        if not cleaned:
            continue

        items.append(f"<li>{cleaned}</li>")

    if not items:
        return original_block

    return (
        '<ol class="math-generic-list">\n'
        + "\n".join(items)
        + "\n</ol>"
    )


def render_latex_generic_lists_to_html(html: str) -> str:
    """
    Convert generic LaTeX list environments into HTML lists.
    """
    return re.sub(
        r"\\begin\{list\}(?:\{[^{}]*\})?(?:\{[^{}]*\})?([\s\S]*?)\\end\{list\}",
        render_latex_generic_list_block,
        html,
        flags=re.IGNORECASE,
    )


def render_latex_bibliography_block(match: re.Match) -> str:
    """
    Convert a LaTeX thebibliography block into an HTML references section.

    Handles:
        \\bibitem{key} Reference text
        \\bibitem[Label]{key} Reference text

    Fail-safe rule:
        If parsing fails, return the original LaTeX block.
    """
    original_block = match.group(0)
    body = match.group(1).strip()

    if not body:
        return original_block

    bibitem_pattern = re.compile(
        r"\\bibitem(?:\[[^\]]*\])?\{([^{}]*)\}\s*",
        flags=re.IGNORECASE,
    )

    bibitem_matches = list(bibitem_pattern.finditer(body))

    if not bibitem_matches:
        return original_block

    items = []

    for index, bib_match in enumerate(bibitem_matches):
        item_start = bib_match.end()

        if index + 1 < len(bibitem_matches):
            item_end = bibitem_matches[index + 1].start()
        else:
            item_end = len(body)

        cleaned = body[item_start:item_end].strip()

        if cleaned:
            items.append(f"<li>{cleaned}</li>")

    if not items:
        return original_block

    return (
        '<section class="math-bibliography">\n'
        '<h3>References</h3>\n'
        '<ol>\n'
        + "\n".join(items)
        + "\n</ol>\n"
        "</section>"
    )


def render_latex_bibliography_to_html(html: str) -> str:
    """
    Convert LaTeX thebibliography environments into an HTML references section.
    """
    return re.sub(
        r"\\begin\{thebibliography\}(?:\{[^{}]*\})?([\s\S]*?)\\end\{thebibliography\}",
        render_latex_bibliography_block,
        html,
        flags=re.IGNORECASE,
    )


def clean_latex_table_cell(cell: str) -> str:
    """
    Clean one LaTeX table cell before wrapping it in <td>.
    """
    cell = cell.strip()

    # Defensive cleanup in case previously-rendered HTML sneaks in.
    cell = re.sub(r"</?p[^>]*>", "", cell, flags=re.IGNORECASE)
    cell = re.sub(r"<br\s*/?>", "", cell, flags=re.IGNORECASE)

    # Remove table rule commands that should not be visible as text.
    cell = re.sub(r"\\hline\b", "", cell, flags=re.IGNORECASE)
    cell = re.sub(r"\\cline\{[^{}]*\}", "", cell, flags=re.IGNORECASE)

    return cell.strip()


def render_latex_tabular_block(match: re.Match) -> str:
    """
    Convert a simple LaTeX tabular environment into an HTML table.

    This intentionally handles the common PlanetMath-style cases:
        \\begin{tabular}{|c|c|}
        \\hline
        A & B \\\\
        \\hline
        1 & 2
        \\end{tabular}

    Fail-safe rule:
        If parsing produces no usable rows, return the original block.
    """
    original_block = match.group(0)
    body = match.group(2).strip()

    if not body:
        return original_block

    # Defensive cleanup in case this function is ever run on already-rendered text.
    body = re.sub(
        r"<br\s*/?>\s*</p>\s*<p[^>]*>",
        r"\\\\",
        body,
        flags=re.IGNORECASE,
    )
    body = re.sub(r"</?p[^>]*>", "", body, flags=re.IGNORECASE)
    body = re.sub(r"<br\s*/?>", r"\\\\", body, flags=re.IGNORECASE)

    # Split rows on LaTeX row breaks.
    raw_rows = re.split(
        r"\\\\(?:\s*\[[^\]]*\])?",
        body,
        flags=re.IGNORECASE,
    )

    html_rows = []

    for raw_row in raw_rows:
        row = raw_row.strip()

        if not row:
            continue

        row = re.sub(r"\\hline\b", "", row, flags=re.IGNORECASE)
        row = re.sub(r"\\cline\{[^{}]*\}", "", row, flags=re.IGNORECASE)
        row = row.strip()

        if not row:
            continue

        raw_cells = row.split("&")
        cells = [clean_latex_table_cell(cell) for cell in raw_cells]

        # Avoid creating empty junk rows.
        if not any(cells):
            continue

        html_cells = "".join(f"<td>{cell}</td>" for cell in cells)

        html_rows.append(f"<tr>{html_cells}</tr>")

    if not html_rows:
        return original_block

    return (
        '<div class="math-table-wrapper">\n'
        '<table class="math-table">\n'
        '<tbody>\n'
        + "\n".join(html_rows)
        + "\n</tbody>\n"
        "</table>\n"
        "</div>"
    )


def render_latex_tabular_to_html(html: str) -> str:
    """
    Convert simple LaTeX tabular environments into HTML tables.
    """
    return re.sub(
        r"\\begin\{tabular\}(\{[^{}]*\})?([\s\S]*?)\\end\{tabular\}",
        render_latex_tabular_block,
        html,
        flags=re.IGNORECASE,
    )


SIMPLE_BLOCK_ENVIRONMENTS = {
    "center": ("div", "math-center"),
    "quote": ("blockquote", "math-quote"),
    "multicols": ("div", "math-multicols"),
    "figure": ("figure", "math-figure"),
    "minipage": ("div", "math-minipage"),
    "document": ("div", "math-document"),
}


def render_simple_latex_block_environment(match: re.Match) -> str:
    """
    Convert simple display/block LaTeX environments into HTML wrappers.

    Examples:
        \\begin{center} ... \\end{center}
        \\begin{quote} ... \\end{quote}
    """
    original_block = match.group(0)
    env_name = match.group(1).lower()
    body = match.group(2).strip()

    if env_name not in SIMPLE_BLOCK_ENVIRONMENTS or not body:
        return original_block

    html_tag, css_class = SIMPLE_BLOCK_ENVIRONMENTS[env_name]

    return (
        f'<{html_tag} class="{css_class}">\n'
        f'{body}\n'
        f'</{html_tag}>'
    )


def render_simple_latex_block_environments_to_html(html: str) -> str:
    """
    Convert simple non-math LaTeX block environments into HTML.

    This supports simple begin arguments such as:
        \\begin{figure}[h]
        \\begin{multicols}{2}
        \\begin{minipage}{0.5\\textwidth}

    The arguments are intentionally ignored for now.
    """
    env_names = "|".join(
        re.escape(name)
        for name in sorted(SIMPLE_BLOCK_ENVIRONMENTS.keys(), key=len, reverse=True)
    )

    pattern = re.compile(
        rf"\\begin\{{({env_names})\}}"
        rf"(?:\[[^\]]*\])?"
        rf"(?:\{{[^{{}}]*\}})*"
        rf"([\s\S]*?)"
        rf"\\end\{{\1\}}",
        flags=re.IGNORECASE,
    )

    return pattern.sub(render_simple_latex_block_environment, html)


THEOREM_LIKE_ENVIRONMENTS = {
    "proof": "Proof",
    "prop": "Proposition",
    "thm": "Theorem",
    "thm*": "Theorem",
    "theorem": "Theorem",
    "lem": "Lemma",
    "lemma": "Lemma",
    "lemma*": "Lemma",
    "cor": "Corollary",
    "defn": "Definition",
    "definition": "Definition",
    "rem": "Remark",
    "example": "Example",
    "example*": "Example",
}


def render_latex_named_environment_block(match: re.Match) -> str:
    """
    Convert theorem/proof-like LaTeX environments into semantic HTML blocks.

    Example:
        \\begin{proof}
        ...
        \\end{proof}

    becomes:
        <section class="math-env math-env-proof">
        <div class="math-env-label">Proof.</div>
        <div class="math-env-body">...</div>
        </section>
    """
    original_block = match.group(0)
    env_name = match.group(1)
    body = match.group(2).strip()

    env_key = env_name.lower()
    label = THEOREM_LIKE_ENVIRONMENTS.get(env_key)

    if not label or not body:
        return original_block

    css_key = re.sub(r"[^a-z0-9]+", "-", env_key).strip("-")

    return (
        f'<section class="math-env math-env-{css_key}">\n'
        f'<div class="math-env-label">{label}.</div>\n'
        f'<div class="math-env-body">\n{body}\n</div>\n'
        f'</section>'
    )


def render_latex_named_environments_to_html(html: str) -> str:
    """
    Convert theorem/proof-like LaTeX environments into HTML.

    This intentionally does not touch MathJax environments like equation,
    align, pmatrix, array, cases, etc.
    """
    env_names = "|".join(
        re.escape(name)
        for name in sorted(THEOREM_LIKE_ENVIRONMENTS.keys(), key=len, reverse=True)
    )

    pattern = re.compile(
        rf"\\begin\{{({env_names})\}}([\s\S]*?)\\end\{{\1\}}",
        flags=re.IGNORECASE,
    )

    return pattern.sub(render_latex_named_environment_block, html)


def render_latex_description_block(match: re.Match) -> str:
    """
    Convert a LaTeX description environment into an HTML description list.

    Example:
        \\begin{description}
        \\item[Term] Explanation
        \\item[Other] More explanation
        \\end{description}

    becomes:
        <dl class="math-description-list">
        <dt>Term</dt>
        <dd>Explanation</dd>
        ...
        </dl>
    """
    original_block = match.group(0)
    body = match.group(1).strip()

    if not body:
        return original_block

    item_pattern = re.compile(
        r"\\item(?:\s*\[([^\]]*)\])?\s*",
        flags=re.IGNORECASE,
    )

    item_matches = list(item_pattern.finditer(body))

    if not item_matches:
        return original_block

    entries = []

    for index, item_match in enumerate(item_matches):
        label = (item_match.group(1) or "").strip()
        item_start = item_match.end()

        if index + 1 < len(item_matches):
            item_end = item_matches[index + 1].start()
        else:
            item_end = len(body)

        description = body[item_start:item_end].strip()

        if not label and not description:
            continue

        if label:
            entries.append(f"<dt>{label}</dt>")
            entries.append(f"<dd>{description}</dd>")
        else:
            entries.append(f"<dd>{description}</dd>")

    if not entries:
        return original_block

    return (
        '<dl class="math-description-list">\n'
        + "\n".join(entries)
        + "\n</dl>"
    )


def render_latex_description_to_html(html: str) -> str:
    """
    Convert LaTeX description environments into HTML description lists.
    """
    return re.sub(
        r"\\begin\{description\}([\s\S]*?)\\end\{description\}",
        render_latex_description_block,
        html,
        flags=re.IGNORECASE,
    )


def find_matching_latex_brace(text: str, open_brace_index: int) -> int:
    """
    Find the matching closing brace for a LaTeX command argument.

    Returns:
        The index of the matching closing brace, or -1 if no match is found.
    """
    depth = 0

    for index in range(open_brace_index, len(text)):
        char = text[index]

        if char == "{":
            depth += 1
        elif char == "}":
            depth -= 1

            if depth == 0:
                return index

    return -1


def replace_latex_text_command(
    html: str,
    command_name: str,
    open_tag: str,
    close_tag: str,
) -> str:
    """
    Replace a LaTeX text command with an HTML tag, while allowing nested braces.

    Example:
        \\emph{$\\mathcal{R}$-closed}

    becomes:
        <em>$\\mathcal{R}$-closed</em>
    """
    command_start = f"\\{command_name}" + "{"
    search_start = 0
    output_parts = []

    while True:
        start_index = html.find(command_start, search_start)

        if start_index == -1:
            output_parts.append(html[search_start:])
            break

        open_brace_index = start_index + len(command_start) - 1
        close_brace_index = find_matching_latex_brace(html, open_brace_index)

        if close_brace_index == -1:
            output_parts.append(html[search_start:])
            break

        inner_text = html[open_brace_index + 1:close_brace_index]

        output_parts.append(html[search_start:start_index])
        output_parts.append(f"{open_tag}{inner_text}{close_tag}")

        search_start = close_brace_index + 1

    return "".join(output_parts)


def render_prose_latex_to_html(tex: str) -> str:
    if not tex:
        return ""

    # Normalize Windows/Mac line endings to Unix-style newlines so later regex
    # replacements behave consistently.
    html = tex.replace("\r\n", "\n").replace("\r", "\n")

    # Convert larger LaTeX block environments before paragraph wrapping.
    # Order matters: list/table/environment renderers should run before generic
    # line-break and paragraph conversion.
    html = render_latex_lists_to_html(html)
    html = render_latex_generic_lists_to_html(html)
    html = render_latex_description_to_html(html)
    html = render_latex_tabular_to_html(html)
    html = render_latex_bibliography_to_html(html)
    html = render_latex_named_environments_to_html(html)
    html = render_simple_latex_block_environments_to_html(html)

    # Convert PlanetMath explicit links:
    #   \PMlinkname{visible text}{TargetSlug}
    # into normal clickable concept links.
    html = re.sub(
        r"\\PMlinkname\{([^{}]+)\}\{([^{}]+)\}",
        render_pmlinkname,
        html,
        flags=re.DOTALL,
    )

    # Preserve PlanetMath no-autolink text as a span that the frontend autolinker
    # can intentionally skip.
    html = re.sub(
        r"\\PMlinkescapetext\{([^{}]*)\}",
        r'<span class="math-no-autolink">\1</span>',
        html,
        flags=re.DOTALL,
    )

    # Convert LaTeX text commands that use braced arguments.
    # Use the balanced-brace helper instead of simple regex so nested content such as
    #   \textbf{\emph{points}}
    #   \emph{$\mathcal{R}$-closed}
    # is handled correctly.
    html = replace_latex_text_command(html, "textbf", "<strong>", "</strong>")
    html = replace_latex_text_command(html, "emph", "<em>", "</em>")
    html = replace_latex_text_command(html, "textit", "<em>", "</em>")

    # Convert older TeX-style emphasis:
    #   {\em ...}
    # into semantic HTML emphasis.
    html = re.sub(
        r"\{\\em\s+([^{}]*)\}",
        r"<em>\1</em>",
        html,
        flags=re.DOTALL,
    )

    # Convert older TeX-style italics:
    #   {\it ...}
    # into semantic HTML emphasis.
    html = re.sub(
        r"\{\\it\s+([^{}]*)\}",
        r"<em>\1</em>",
        html,
        flags=re.DOTALL,
    )

    # Convert older TeX-style bold:
    #   {\bf ...}
    # into semantic HTML bold text.
    html = re.sub(
        r"\{\\bf\s+([^{}]*)\}",
        r"<strong>\1</strong>",
        html,
        flags=re.DOTALL,
    )

    # Convert LaTeX paragraph headings into inline bold labels.
    html = re.sub(
        r"\\paragraph\*?\{([^{}]*)\}",
        r"<strong>\1.</strong>",
        html,
        flags=re.DOTALL,
    )

    # Convert LaTeX subsubsections into still-lower-level HTML headings.
    html = re.sub(
        r"\\subsubsection\*?\{([^{}]*)\}",
        r"<h4>\1</h4>",
        html,
        flags=re.DOTALL,
    )

    # Convert LaTeX subsections into lower-level HTML headings.
    html = re.sub(
        r"\\subsection\*?\{([^{}]*)\}",
        r"<h3>\1</h3>",
        html,
        flags=re.DOTALL,
    )

    # Convert top-level LaTeX sections into HTML headings.
    html = re.sub(
        r"\\section\*?\{([^{}]*)\}",
        r"<h2>\1</h2>",
        html,
        flags=re.DOTALL,
    )

    # Drop LaTeX labels for now.
    # These are internal anchors for \ref, but we are not building anchor targets yet.
    html = re.sub(
        r"\\label\{[^{}]*\}",
        "",
        html,
        flags=re.DOTALL,
    )

    # Convert LaTeX references into visible fallback text.
    # Later, this could become a real link if we build label/anchor support.
    html = re.sub(
        r"\\ref\{([^{}]*)\}",
        r"\1",
        html,
        flags=re.DOTALL,
    )

    # Convert LaTeX citations into simple visible fallback text.
    # Later, this could link to rendered bibliography entries.
    html = re.sub(
        r"\\cite\{([^{}]*)\}",
        r"[citation: \1]",
        html,
        flags=re.DOTALL,
    )

    # Convert LaTeX forced line breaks followed by a blank line into paragraph breaks.
    html = re.sub(r"\\\\[ \t]*(?:\n[ \t]*){2,}", "\n\n", html)

    # Convert remaining LaTeX forced line breaks into HTML line breaks.
    html = re.sub(r"\\\\[ \t]*", "<br>\n", html)

    # Remove empty list items that can result from unusual nested list structures.
    html = remove_empty_html_list_items(html)

    # Split prose into paragraphs on blank lines.
    parts = re.split(r"\n\s*\n+", html.strip())
    paragraphs = []

    # Wrap each non-empty prose block in a paragraph.
    for part in parts:
        cleaned = part.strip()
        if cleaned:
            paragraphs.append(f"<p>{cleaned}</p>")

    return "\n\n".join(paragraphs)